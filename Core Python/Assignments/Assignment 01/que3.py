# WAP to find quotient and remainder of two numbers

#Take input from user
num1=int(input('enter num1:'))
num2=int(input('enter num2:'))

#Calculate quotient
quotient = num1//num2
print('quotient of given two numbers is', quotient)

#Calculate remainder
reminder = num1%num2

#Display result
print('Reminder of given two number is' , reminder)