####Bitwise Op
#1. &(AND) : if both bits are 1 then consider otherwise 0
print(12 & 6)

#2. |(OR) :if both bits are 0 then consider otherwise 1
print(12 | 6)

#3. ^(XOR) : if both bits are different then consider 1 otherwise(same) 0
print(12 ^ 6)

#4. ~(NOT) : 1's complement         (tillt)
print(~10)   # -(10+1) : -(x+1)

#5. >>(right shift)
print(20 >> 2)

#6. <<(left shift)  
print(6 << 3)